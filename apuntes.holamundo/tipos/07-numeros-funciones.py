import math

print(round(1.3))  # REDONDEAR
print(abs())  # VALOR ABSOLUTO DEL NUMERO

# LLEVA EL NUMERO AL NUMERO ENTERO SUPERIOR MAS CERCANO, ES DECIR 2
print(math.ceil(1.1))
print(math.floor(1.9))  # LO MISMO PERO HACIA ABAJO
print(math.pow(10, 3))  # ELEVA LOS NUMERO, EN ESTE CASO 10 ELEVADO 3
print(math.sqrt(9))  # SACA RAIZ CUADRADA
